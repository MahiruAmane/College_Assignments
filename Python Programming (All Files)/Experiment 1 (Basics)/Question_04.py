# Write a Python Program To Perform Basic Arithmetic Operations On Two Numbers.

a = int(input("Please Enter The First Number : "))
b = int(input("Please Enter The Second Number : "))

result = a + b
print("The Addition Of {} & {} Is : ".format(a, b), result)

result = a - b
print("The Subtraction Of {} & {} Is : ".format(a, b), result)

result = a * b
print("The Multiplication Of {} & {} Is : ".format(a, b), result)

result = a / b
print("The Division Of {} & {} Is : ".format(a, b), result)